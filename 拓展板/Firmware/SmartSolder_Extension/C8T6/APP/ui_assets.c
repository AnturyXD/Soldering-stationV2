static const uint8_t *const *GetPhrase(UiTextZh_t text, uint8_t *count)
{
    switch (text)
    {
    case UI_TEXT_ZH_WORKING:
        *count = 3U;
        return phrase_working;
    case UI_TEXT_ZH_ONLINE:
        *count = 2U;
        return phrase_online;
    case UI_TEXT_ZH_OFFLINE:
        *count = 2U;
        return phrase_offline;
    case UI_TEXT_ZH_MANUAL:
        *count = 2U;
        return phrase_manual;
    case UI_TEXT_ZH_AUTO:
        *count = 2U;
        return phrase_auto;
    case UI_TEXT_ZH_LIGHT:
        *count = 2U;
        return phrase_light;
    case UI_TEXT_ZH_FAN:
        *count = 2U;
        return phrase_fan;
    default:
        *count = 0U;
        return 0;
    }
}

static void DrawGlyph16(uint16_t x, uint16_t y, const uint8_t *data, uint16_t fg, uint16_t bg)
{
    uint8_t row;
    uint8_t col;
    uint8_t byteValue;

    if (data == 0)
    {
        return;
    }

    for (row = 0U; row < UI_ZH_FONT_H; row++)
    {
        for (col = 0U; col < UI_ZH_FONT_W; col++)
        {
            byteValue = data[row * 2U + col / 8U];
            ST7789_DrawPixel((uint16_t)(x + col),
                             (uint16_t)(y + row),
                             (byteValue & (uint8_t)(0x80U >> (col % 8U))) ? fg : bg);
        }
    }
}

uint16_t UiAssets_TextWidth16(UiTextZh_t text)
{
    uint8_t count;

    (void)GetPhrase(text, &count);
    return (uint16_t)(count * UI_ZH_FONT_W);
}

void UiAssets_DrawText16(uint16_t x, uint16_t y, UiTextZh_t text, uint16_t fg, uint16_t bg)
{
    uint8_t count;
    uint8_t i;
    const uint8_t *const *phrase = GetPhrase(text, &count);

    for (i = 0U; i < count; i++)
    {
        DrawGlyph16((uint16_t)(x + i * UI_ZH_FONT_W), y, phrase[i], fg, bg);
    }
}
